// ARRAY
// ********************************************
var names = ['anjana', 'ann', 'malu'];
console.log(names.length);// no of elements in an array

//index is used to access an element in an arraaaaaaaaaaay
names[0];

names[3];

console.log(names[0]);

// Basic methods used in an array
console.log("push"); // add to last

names.push('joel');
console.log(names);
// *******************************************
console.log("pop");

names.pop();     //remove from last
console.log(names);
// *************************************************
console.log("shift");

names.shift();  //remove form front
console.log(names);
// **********************************************************
console.log("unshift");

names.unshift('joel'); // add to front
console.log(names);

